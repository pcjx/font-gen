void displayText(const char* text) {
    while (*text != '\0') {
        unsigned char charCode = (unsigned char)(*text);

        if (charCode < Font7x10.font_MinChar || charCode > Font7x10.font_MaxChar) {
            charCode = 126;  // Use the unknown character '~'
        }

        int startIndex = (charCode - 32) * Font7x10.font_BPC;
        int endIndex = startIndex + Font7x10.font_BPC;

		for (int i = 0; i < Font7x10.font_BPC; i++) {
			for (int j = 1; j < Font7x10.font_Width + 1; j++) {
				char bit_value = (Font7x10.font_Data[startIndex + i] >> j) & 1;
				if (bit_value) {
					printf("#");
				}
				else {
					printf(" ");
				}
			}
			printf("\n");
		}



        text++;
    }
}

